//要将"China"译成密码，译码规律是：用原来字母后面的第4个字母代替原来的字母．
//例如，字母"A"后面第4个字母是"E"．"E"代替"A"。因此，"China"应译为"Glmre"。
//请编一程序，用赋初值的方法使cl、c2、c3、c4、c5五个变量的值分别为，
//’C’、’h’、’i’、’n’、’a’，经过运算，
//使c1、c2、c3、c4、c5分别变为’G’、’l’、’m’、’r’、’e’，并输出。
#include <stdio.h>

int main()
{
	char c1 = 'C', c2 = 'h', c3 = 'i', c4 = 'n', c5 = 'a';
	c1 = c1 + 4;
	c2 = c2 + 4;
	c3 = c3 + 4;
	c4 = c4 + 4;
	c5 = c5 + 4;
	printf("%c%c%c%c%c\n", c1, c2, c3, c4, c5);

	return 0;
}
//单引号：在 C 语言中，表示单个字符必须用单引号，比如 'C'。如果不加引号，电脑会认为 C 是一个变量名。
//字符即数字：这是你理解这道题的钥匙——在计算机眼里，字符就是数字（ASCII码）。
//1. 核心原理：ASCII 码的秘密
//字符 'A' 在内存里存的其实是数字 65。
//字符 'B' 存的是 66。
//所以，如果你执行 'A' + 4，结果就是 69，而 69 正好对应字符 'E'。