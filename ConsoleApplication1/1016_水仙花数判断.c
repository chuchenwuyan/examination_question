#include <stdio.h>

int main()
{

    // 1. 遍历所有的三位数
    for (int i = 100; i <= 999; i++)
    {
        // 2. 每次拿到一个 i，就把它“大卸八块”（一定要写在循环里面！）
        int a = i / 100;           // 提取百位
        int b = (i / 10) % 10;     // 提取十位
        int c = i % 10;            // 提取个位

        // 3. 构造判断条件：各位数字的立方和 是否等于 它本身(i)
        // 注意：等于要用 ==，乘方要手写 a*a*a
        if ((a * a * a) + (b * b * b) + (c * c * c) == i)
        {
            // 如果相等，说明找到了水仙花数，打印出来，并换行
            printf("%d\n", i);
        }
    }

    return 0;
}