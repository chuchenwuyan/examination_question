//有一头母牛，它每年年初生一头小母牛。
//每头小母牛从第四个年头开始，每年年初也生一头小母牛。
//请编程实现在第n年的时候，共有多少头母牛？n(0<n<55)，n的含义如题目中描述。
//n = 0表示输入数据的结束，不做处理。
#include <stdio.h>

int main()
{
    int n;
    // 定义一个数组（56个格子），用来存 1-55 年的答案
    // long long 保证数字很大时也不会崩
    long long f[56];

    // 1. 先把前 4 年写死（这是基础）
    f[1] = 1;
    f[2] = 2;
    f[3] = 3;
    f[4] = 4;

    // 2. 递推计算出剩下的 5-55 年
    // 公式：今年的 = 昨天的 + 三年前的
    for (int i = 5; i <= 55; i++) {
        f[i] = f[i - 1] + f[i - 3];
    }

    // 3. 处理输入
    // 只要读入的 n 不是 0，就一直循环
    while (scanf_s("%d", &n) != EOF && n != 0) {
        // 直接从数组里把算好的答案拿出来
        printf("%lld\n", f[n]);
    }

    return 0;
}

